         <h2 class="title2">Who are BlackPink?</h2>

         <p>Blackpink (Hangul: 블랙핑크; stylized as BLACKPINK or BLΛƆKPIИK) is a South Korean girl group formed by
         
             YG Entertainment, consisting of members Jisoo, Jennie, Lisa, and Rosé.</p>

         <p>The group debuted on August 8, 2016, with their single album Square One, which spawned Whistle,
             their first number-one song in South Korea, as well as Boombayah, their first number-one hit on
             the Billboard World Digital Songs chart, which set the record as the most-viewed debut music video
             by a Korean act. With the group's early commercial success, they were hailed as the New Artist of
             the Year at the 31st Golden Disc Awards and the 26th Seoul Music Awards.

             As of 2018, Blackpink is the highest-charting female K-pop act on both Billboard Hot 100 and
             Billboard 200, peaking at number 55 with Ddu-Du Ddu-Du, and peaking at number 40 with Square Up,
             respectively. They are also the first and only K-pop girl group to enter and top Billboard's
             Emerging Artists chart. They are also the first female K-pop group to have four number-one singles
             on Billboard's World Digital Song Sales chart. At the time of its release, Ddu-Du Ddu-Du was the
             most-viewed music video in 24 hours on YouTube, and in January 2019, it became the most viewed
             music
             video by a K-pop group on YouTube.</p>
