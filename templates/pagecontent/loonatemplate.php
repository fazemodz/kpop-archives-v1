        <h2 class="title2">2018–present: Debut with [+ +]</h2>
        <p>
            On August 7, 2018, Loona released Favorite as the lead single from [+ +] (read as plus plus), their
            debut
            extended play as a full group. The extended play was released on August 20, 2018 alongside the second
            single, Hi High. [+ +] was repackaged as [x x] on February 19, 2019, with six additional tracks,
            including a new single, Butterfly.
        </p>

