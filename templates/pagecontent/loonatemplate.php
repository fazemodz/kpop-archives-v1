            <h2 class="title2">Who are LOOΠΔ?</h2>
            <p>
                Loona (stylized as LOOΠΔ) is a South Korean girl group formed by Blockberry Creative. Its twelve members
                were revealed in a periodic fashion, corresponding to their Korean name Idarui Sonyeo (이달의 소녀), which
                translates to Girl of the Month.

                Loona commenced their pre-debut project in August 2016, with each member releasing a promotional single
                over
                the next eighteen months.The group has formed three sub-units: Loona 1/3, Loona Odd Eye Circle and Loona
                yyxy. Their debut EP as a full ensemble, [+ +] (2018), was supported by the lead single Hi High.
            </p>
        </div>
        
            <h2 class="title2">Why is their Name styled like LOOΠΔ </h2>
            <p>Loona','s English name is derived from the Hangul letters ㅇㄷㅇㅅㄴ, each an initial consonant in the syllabic
                blocks that make 이달의 소녀 (Idarui Sonyeo). When rearranged to ㄴㅇㅇㄷㅅ, it resembles LOONA in the Latin
                alphabet.
            </p>
        </div>
        </div>

    </section>

    <section class="section">
        <h2 class="title2">2016–2018: Pre-debut project and Mix Nine</h2>


        <p>
            On October 2, 2016, Blockberry Creative announced through Naver that they would be debuting their
            first girl
            group through an 18-month long pre-debut project. Between October 2016 and January 2017, four
            members
            (HeeJin, HyunJin, HaSeul, and YeoJin) were revealed; each member released a single album typically
            consisting of a solo track and another track performed with other revealed members, under the
            group','s Korean
            name Idarui Sonyeo (Hangul: 이달의 소녀; lit. Girl of the Month) In March 2017, the group','s first
            sub-unit,
            Loona 1/3, was introduced, consisting of previously revealed members HeeJin, HyunJin, HaSeul and a
            new
            member, ViVi. Loona 1/3 released the extended play Love & Live and accompanying single of the same
            name
            on March 13, 2017,with a television promotion on SBS','s Inkigayo, They released a repackaged edition
            of the extended play, titled Love & Evil, on April 27.</p>
        </div>
        
        

        <p>
            Between April 2017 and July 2017, members ViVi, Kim Lip, JinSoul, and Choerry released their single
            albums,
            continuing the same Girl of the Month pattern. In September 2017, three members, HeeJin, HyunJin and
            HaSeul, auditioned for JTBC','s reality show Mix Nine; HeeJin and HyunJin passed the audition stage
            making it
            onto the show, where HyunJin placed Top 18 and HeeJin placed fourth in the finale.The group','s
            second sub-unit, Loona Odd Eye Circle, consisting of JinSoul, Kim Lip and Choerry, released the
            extended
            play Mix & Match on September 21, 2017, and began promotions on Mnet','s M Countdown. The English
            version
            of their song Loonatic was released on October 23. On October 31 they released a repackaged
            edition of
            the extended play, titled Max & Match, which included three new songs.</p2>
        </div>
       

        <p>
            Between the months of November 2017 and January 2018, they released the solo single albums for new
            members
            Yves, Chuu, and Go Won, continuing the monthly pattern. In March 2018, they released the solo single
            album for final member, Olivia Hye. On May 30, 2018, the group','s third sub-unit, Loona yyxy, made
            their
            debut with the extended play Beauty & the Beat. The single love4eva featuring Canadian musician
            Grimes was
            released the same day with an accompanying music video.
        </p>

        </div>

