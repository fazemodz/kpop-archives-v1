<div class="ThePlot">
    <p>A young woman with bad premonition dreams meets two people who suddenly develop the same ability.</br>

</br>

Nam Hong Joo (<a href="#Suzy">Suzy</a>) lives with her mother, Yoon Moon Sun (<a href="#Hwang-Young-Hee">Hwang Young Hee</a>), a widow who runs a small restaurant. Jung Jae Chan (<a href="#Lee-Jong-Suk">Lee Jong Suk</a>), a rookie prosecutor, and his younger brother, Seung Won (<a href="#Shin-Jae-Ha">Shin Jae Ha</a>), move in across the street. Since she was young, Hong Joo has had the ability to see bad events before they happen, but she is often unable to do anything about it.</br>
</br>


One day, Jae Chan has a strange premonition dream about an accident involving Hong Joo and Lee Yoo Beom (<a href="#Lee-Sang-Yeob">Lee Sang Yeob</a>), a ruthless attorney who used to be Jae Chan’s tutor. Jae Chan decides to interfere in the course of events and ends up saving the lives of Hong Joo and Han Woo Tak (<a href="#Jung-Hae-In">Jung Hae In</a>), a young police officer. When Jae Chan, Hong Joo and Woo Tak then start having dreams about one another, they realize that their lives are now somehow entwined.</br>

</br>

But can the three discover the reason that they were brought together, and can they prevent the people closest to them from getting hurt?</br>

</br>

“While You Were Sleeping” is a 2017 South Korean drama series directed by Oh Choong Hwan. It is not related to the 1995 American film by the same title starring Sandra Bullock. The script is written by Park Hye Ryun, who previously worked with Suzy on her acting debut in the television drama “<a href="https://www.viki.com/tv/902c-dream-high" target="blank">Dream High</a>” (2011) and with Lee Jong Suk in the dramas “<a href="https://www.viki.com/tv/12147c-i-hear-your-voice" target="blank2">I Hear Your Voice</a>” (2013) and “<a href="https://www.viki.com/tv/23066c-pinocchio" target="blank3">Pinocchio</a>” (2014).</br></p>
