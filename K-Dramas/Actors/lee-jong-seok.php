     <h2 class="title">Who is lee jong seok?</h2>
     <p>Lee Jong Suk is a South Korean actor and model who is considered one of the most popular Hallyu stars working today. 
     Born on September 14, 1989, Lee Jong Suk started out as a model, becoming the youngest Korean runway model at the age of 15. 
     He quickly crossed over into acting, debuting in the 2010 dramas “Prosecutor Princess” and gaining acclaim for his role as a rebellious composer in “Secret Garden.” 
     But his status skyrocketed after appearing as the lead in the 2012 drama “School 2013,” for which he received his first acting award for Best New Actor. His leading role in “I Hear Your Voice” 
     made him a household name and one of the most sought-after actors in Korea. He went on to star in the hit dramas “Doctor Stranger” (2014), “Pinocchio” (2014-2015), “W” (2016) and “While You Were Sleeping” (2017).

