     <h2 class="title">Who is Suzy?</h2>
     <p>Suzy, the stage name of Bae Su Ji or Bae Suzy, is a popular South Korean singer and actress. Born on October 10, 1994, she is a member of the South Korean girl group Miss A.
     Suzy was an online shopping model before she participated in the reality competition “Superstar K.” 
     Although she only made it through the preliminary round, she caught the attention of a scout and soon after became a music trainee for JYP Entertainment. 
     She trained for only a year before she debuted with Miss A in 2010. That same year, Suzy also became one of the hosts for the music variety show “MBC Show! Music Core” and went on to host many other music shows.
     She branched into acting with the 2011 television drama “Dream High” and is best known for her roles in the dramas “Gu Family Book” (2013), “<a href="../uncontrollably-fond.php">Uncontrollably Fond</a>” (2016) and “<a href="../while-you-were-sleeping.php">While You Were Sleeping</a>” (2017). 
     Suzy holds the distinction of being the first Korean female celebrity to have more than 1 million followers on Twitter. 
     In March 2015, she also made international headlines when it was confirmed by her agency that she is dating Korean megastar Lee Min Ho.</p>
